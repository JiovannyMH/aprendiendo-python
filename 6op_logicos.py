#AND
RESULTADO = True & True # Devuelve verdadero
RESULTADO2 = False & True # Devuelve Falso
RESULTADO3 = True & False # Devuelve Falso
RESULTADO4 = False & False # Devuelve Falso
#OR
RESULTADO5 = True | True # Devuelve Falso
RESULTADO6 = False | True # Devuelve Falso
RESULTADO7 = True | False # Devuelve Falso
RESULTADO8 = False | False # Devuelve verdadero
#NOT
RESULTADO9 = not True # Devuelve Falso
RESULTADO10 = not False # Devuelve verdadero

print("COMPUERTA LOGICA AND")
print(RESULTADO)
print(RESULTADO2)
print(RESULTADO3)
print(RESULTADO4)
print("COMPUERTA LOGICA OR")
print(RESULTADO5)
print(RESULTADO6)
